unsigned int noc_find_addr_from_routeid(unsigned int idx, int initflow, int targetflow, int targetsubrange)
{
    unsigned int i;
    unsigned int count;
    ROUTE_ID_ADDR_STRU* pastTbl;

    if (0 == idx)
    {
        count = (sizeof(hi6xxx_sysnoc_routeid_addr_tbl)/sizeof(ROUTE_ID_ADDR_STRU));
        pastTbl = (ROUTE_ID_ADDR_STRU*)hi6xxx_sysnoc_routeid_addr_tbl;
    }
    else if (1 == idx) {
        count = (sizeof(hi6xxx_medianoc_routeid_addr_tbl)/sizeof(ROUTE_ID_ADDR_STRU));
        pastTbl = (ROUTE_ID_ADDR_STRU*)hi6xxx_medianoc_routeid_addr_tbl;
    }
    else {
        return 0;
    }

    for(i = 0 ; i < count ; i++)
    {
        if (
        (pastTbl[i].targ_flow == targetflow) &&
        (pastTbl[i].targ_subrange == targetsubrange))
        return pastTbl[i].init_localaddr;
    }
    return 0;
}

